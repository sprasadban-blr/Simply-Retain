package com.simplyretain.domain;

public class Characteristics {
	
	
	private String gender;
	private String age_category;
	private String marital;
	private String monthlyIncome;
	private String salaryHike;
	private String perf_rating;
	private String recognition;
	
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAge_category() {
		return age_category;
	}
	public void setAge_category(String age_category) {
		this.age_category = age_category;
	}
	public String getMarital() {
		return marital;
	}
	public void setMarital(String marital) {
		this.marital = marital;
	}
	public String getMonthlyIncome() {
		return monthlyIncome;
	}
	public void setMonthlyIncome(String monthlyIncome) {
		this.monthlyIncome = monthlyIncome;
	}
	public String getSalaryHike() {
		return salaryHike;
	}
	public void setSalaryHike(String salaryHike) {
		this.salaryHike = salaryHike;
	}
	public String getPerf_rating() {
		return perf_rating;
	}
	public void setPerf_rating(String perf_rating) {
		this.perf_rating = perf_rating;
	}
	public String getRecognition() {
		return recognition;
	}
	public void setRecognition(String recognition) {
		this.recognition = recognition;
	}
	
	
}
